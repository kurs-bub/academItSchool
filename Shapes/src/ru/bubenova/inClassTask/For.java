package ru.bubenova.inClassTask;

public class For {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            System.out.println(i);
        }

        for (int j = 7; j <= 121; j++) {
            System.out.println(j);
        }

        for (int k = 100; k >= 30; k--) {
            System.out.println(k);
        }
    }
}
